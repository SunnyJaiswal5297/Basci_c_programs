#include<iostream>
using namespace std;

void sudoku(char a[][9])
{
	int i,j;
	for(i=0;i<9;i++)
	{
		for(j=0;j<9;j++)
		{
			if(a[i][j]=='\0')
				a[i][j]='X';
		}
	}
	
}
int main()
{
	int i,j;
	char a[9][9]={
		{'\0','\0','\0','2','6','\0','7','\0','1'},
		{'6','8','\0','\0','7','\0','\0','9','\0'},
		{'1','9','\0','\0','\0','4','5','\0','\0'},
		{'8','2','\0','1','\0','\0','\0','4','\0'},
		{'\0','\0','4','6','\0','2','9','\0','\0'},
		{'\0','5','\0','\0','\0','3','\0','2','8'},
		{'\0','\0','9','3','\0','\0','\0','7','4'},
		{'\0','4','\0','\0','5','\0','\0','3','6'},
		{'7','\0','3','\0','1','8','\0','\0','\0'}
	};
	cout<<"Given unsolved sudoku is:\n";
	for(i=0;i<9;i++)
	{
		for(j=0;j<9;j++)
		{
			cout<<a[i][j]<<" ";
		}
		cout<<"\n";
	}
	sudoku(a);
	cout<<"solved sudoku is:\n";
	for(i=0;i<9;i++)
	{
		for(j=0;j<9;j++)
		{
			cout<<a[i][j]<<" ";
		}
		cout<<"\n";
	}
	
}
