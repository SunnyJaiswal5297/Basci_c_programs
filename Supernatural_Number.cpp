#include<bits/stdc++.h>
using namespace std;

bool isPrime(int n)
{
    for(int i=2;i*i<=n;i++)
        if(n%i==0)
            return false;
    return true;
}
int main()
{
    int n;
    cin>>n;
    if(n==1 || (n<10 && isPrime(n)))
        cout<<"1"<<endl;
    else if(n>=10 && isPrime(n))
        cout<<"0"<<endl;
    else
    {
        int dp[n+1],i,j;
        memset(dp,0,sizeof(dp));
        dp[1]=0;
        for(i=2;i<=n;i++)
        {
            for(j=2;j<i;j++)
            {
                if(i%j==0)
                {
                    dp[i]+=dp[j];
                    if(j>=10)
                        dp[i]-=j/10;
                }
            }
            if(dp[i]<0)
                dp[i]=0;
            if(i<10)
                dp[i]+=1;
        }
        for(i=1;i<=n;i++)
            cout<<i<<" "<<dp[i]<<endl;
    }
}
