#include<iostream>
#include<cmath>

using namespace std;
int main()
{
	cout<<pow(2,3)<<endl;  //2 raise to the power 3
	cout<<sqrt(16)<<endl;
	cout<<round(4.78)<<endl;  //ceiling function
	cout<<floor(4.78)<<endl; //floor function
	cout<<fmax(10,6)<<endl;
	cout<<fmin(10,6)<<endl;
	return 0;
}
